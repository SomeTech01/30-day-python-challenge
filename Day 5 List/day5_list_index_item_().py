''' list.index(item)
looking for an item's index'''
fruits = ['banana', 'orange', 'mango', 'lemon']
print(fruits)
print('find out what orange\'s index is')
print(fruits.index('orange'))   # 1
ages = [22, 19, 24, 25, 26, 24, 25, 24]
print(ages)
print('find out what 24\'s index is(first occurence only)')
print(ages.index(24))           # 2, the first occurrence
