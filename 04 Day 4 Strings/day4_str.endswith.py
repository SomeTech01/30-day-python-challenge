'''var.endswith(str) check if the var ends with the specific str'''
challenge = 'thirty days of python'
print('Checking if str ends in specific ending')
print(challenge)
print('Check if str ends in "on". Use print(challenge.endswith("on")) ')
print(challenge.endswith('on'))   # True
print('Check if str ends in "tion". Use print(challenge.endswith("tion")) ')
print(challenge.endswith('tion')) # False