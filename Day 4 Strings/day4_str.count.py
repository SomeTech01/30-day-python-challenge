'''var.count(str)
var.count(str,start index range, end index range)'''
challenge = 'thirty days of python'
print(challenge)
print('Check how many times y came up. Use print(challenge.count("y"))')
print(challenge.count('y')) # 3
print('Check how many times y came up in index 7- 14. Use print(challenge.count("y",7,14))')
print(challenge.count('y', 7, 14)) # 1,
print('Check how many times th came up. Use print(challenge.count("th"))') 
print(challenge.count('th')) # 2`